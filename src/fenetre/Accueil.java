    private void fetch(){
               try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/missions", "root", "");
            String sql = "SELECT count(*) AS total FROM `missions`";
            String sql2 = "SELECT count(*) AS total1 FROM `conducteurs`";
              //"select * from student where id_number='" + id + "'"
              // + "VALUES ('" + fname + "','" + lname + "','" + id + "')";
            st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
                while (rs.next()) {
                String toalMission;
                toalMission=rs.getString("total");
                jLabel4.setText(toalMission);
            }
            ResultSet rs1 = st.executeQuery(sql2);
            while (rs1.next()) {
                String toalConducteur;
                toalConducteur = rs1.getString("total1");
                jLabel5.setText(rs1.getString("total1"));
            }
            con.close();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(LoginFenetre.class.getName()).log(Level.SEVERE, null, ex);
        }
 
    }
